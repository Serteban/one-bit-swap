export type SessionContextProps = {
  connected: boolean
  loading: boolean
  recovering: boolean
  connect: () => Promise<void>
  disconnect: () => Promise<void>
}

//**************************************************************************//
//                                                                          //
//           #####                                                          //
//          #     #   ####   #    #  #####  ######  #    #  #####           //
//          #        #    #  ##   #    #    #        #  #     #             //
//          #        #    #  # #  #    #    #####     ##      #             //
//          #        #    #  #  # #    #    #         ##      #             //
//          #     #  #    #  #   ##    #    #        #  #     #             //
//           #####    ####   #    #    #    ######  #    #    #             //
//                                                                          //
//**************************************************************************//
export const SessionContext = createContext<SessionContextProps>(
  {} as SessionContextProps
)

//**************************************************************************//
//                                                                          //
//        ######                                                            //
//        #     #  #####    ####   #    #  #  #####   ######  #####         //
//        #     #  #    #  #    #  #    #  #  #    #  #       #    #        //
//        ######   #    #  #    #  #    #  #  #    #  #####   #    #        //
//        #        #####   #    #  #    #  #  #    #  #       #####         //
//        #        #   #   #    #   #  #   #  #    #  #       #   #         //
//        #        #    #   ####     ##    #  #####   ######  #    #        //
//                                                                          //
//**************************************************************************//

export const SessionProvider = (props: AppProps) => {
  const [connected, setConnected] = useState<boolean>(false)
  const [loading, setLoading] = useState<boolean>(false)
  const [recovering, setRecovering] = useState<boolean>(false)

  const router = useRouter()
  const { active } = useWeb3React<Web3Provider>()

  //**************************************************************************//
  //                                                                          //
  //  #####                                                                   //
  // #     #    ##    #       #       #####     ##     ####   #    #   ####   //
  // #         #  #   #       #       #    #   #  #   #    #  #   #   #       //
  // #        #    #  #       #       #####   #    #  #       ####     ####   //
  // #        ######  #       #       #    #  ######  #       #  #         #  //
  // #     #  #    #  #       #       #    #  #    #  #    #  #   #   #    #  //
  //  #####   #    #  ######  ######  #####   #    #   ####   #    #   ####   //
  //                                                                          //
  //**************************************************************************//

  const connect = useCallback(async () => {
    return await sleep()
  }, [])

  const disconnect = useCallback(async () => {
    return await sleep()
  }, [])

  //**************************************************************************//
  //                                                                          //
  //          #######                                                         //
  //          #        ######  ######  ######   ####   #####   ####           //
  //          #        #       #       #       #    #    #    #               //
  //          #####    #####   #####   #####   #         #     ####           //
  //          #        #       #       #       #         #         #          //
  //          #        #       #       #       #    #    #    #    #          //
  //          #######  #       #       ######   ####     #     ####           //
  //                                                                          //
  //**************************************************************************//

  // Si la billetera no estÃ¡ conectada redirigir
  useEffect(
    () => {
      if (!active) {
        router.push('/conectar')
        setConnected(false)
      }
    },
    //eslint-disable-next-line
    [active] //Si pongo router como dependencia se dispara muy seguido
  )

  //**************************************************************************//

  return (
    <SessionContext.Provider
      value={{ connected, loading, recovering, connect, disconnect }}
    >
      {props.children}
    </SessionContext.Provider>
  )
}

